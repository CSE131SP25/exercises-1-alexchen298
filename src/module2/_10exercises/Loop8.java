package module2._10exercises;

public class Loop8
{
    public static void main(String[] args)
    {
    	int x = 10;
    	while (x > 0)
    	{
    		if (x % 2 == 0)
    		{
    			System.out.println(x + " is even.");
    		}
    		else 
    		{
    			System.out.println(x + " is odd.");
    		}
    		x--;
    			
    	}
    }
}
