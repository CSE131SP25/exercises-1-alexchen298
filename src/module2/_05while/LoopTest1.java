package module2._05while;

public class LoopTest1
{
   public static void main(String[] args)
   {
     // 1. initialize the loop variable
     int count = 2;

     // 2. test the loop variable
     while (count <= 10)
     {
        System.out.println(count);
        // 3. change the loop variable
        count++;
     }

   }
}

